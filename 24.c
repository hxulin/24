/**
 * 数组排序：选择排序算法
 */
void sort(int arr[], int length)
{
	int i, j, k, t;
	for(i = 0; i < length - 1; i++)
	{
		for(k = i, j = i + 1; j < length; j++)
		{
			if(arr[k] > arr[j])
			{
				k = j;
			}
		}
		t = arr[i];
		arr[i] = arr[k];
		arr[k] = t;
	}
}

