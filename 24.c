{
    char bstr[strlen(str)];
    memset(bstr, 0, sizeof(bstr));  // 转换缓冲区 
    int i = 0;
    for( ; i < strlen(str); i++)
	{
        if (!strncmp(str + i, oldstr, strlen(oldstr)))
		{
            strcat(bstr, newstr);
            i += strlen(oldstr) - 1;
        }
		else
		{
        	strncat(bstr, str + i, 1);
	    }
    }
    strcpy(str, bstr);
}

