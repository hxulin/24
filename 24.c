/**
 * 去除表达式中多余的括号
 */
void depar(char* solution, int leftPar, int rightPar)
{
	char inner[ARRAY_SIZE] = {0};
	strncpy(inner, solution + leftPar + 1, rightPar - leftPar - 1);
	
	char temp1[ARRAY_SIZE] = {0};
	char temp2[ARRAY_SIZE] = {0};

	int opIndex;
	char opInner;
	
	if (inner[0] == '(')
	{
    	// ...((oxo)Xo)... (X = opInner)
    	opIndex = indexOf(inner, ')', 0) + 1;
  	}
	else if (inner[strlen(inner) - 1] == ')')
	{
    	// ...(ox(oXo))...
    	opIndex = indexOf(inner, '(', 0) - 1;
	}
	else
	{
		// ...(oXo)...
		opIndex = 1;
	}
	opInner = inner[opIndex];
	while (opInner >= '0' && opInner <= '9')
	{
    	opIndex ++;
    	opInner = inner[opIndex];
	}
	
	// opOuter
	char opLeft = 0, opRight = 0;
	if (leftPar > 0)
	{
    	// oX(oxo)... (x = opInner, X = opOuter)
    	opLeft = solution[leftPar - 1];
  	}
	if (rightPar < strlen(solution) - 1)
	{
		// ...(oxo)Xo
		opRight = solution[rightPar + 1];
	}
	if (opInner == '+' || opInner == '-')
	{
    	if (opLeft == '*' || opLeft == '/' || opRight == '*' || opRight == '/')
		{
      		return;
    	}
    	
    	if (opLeft == '-')
		{
      		// o-(o+o)... => o-o-o... , o-(o-o)... => o-o+o...
      		strncpy(temp1, inner, opIndex);
      		strncpy(temp2, inner + opIndex + 1, strlen(inner) - 1);
      		
      		strcpy(inner, temp1);
      		strcat(inner, opInner == '+' ? "-" : "+");
      		strcat(inner, temp2);
    	}
	}
	else if (opLeft == '/')
	{
    	// o/(o*o)... => o/o/o... , o/(o/o)... => o/o*o...
    	strncpy(temp1, inner, opIndex);
      	strncpy(temp2, inner + opIndex + 1, strlen(inner) - 1);
    	
    	strcpy(inner, temp1);
  		strcat(inner, opInner == '*' ? "/" : "*");
  		strcat(inner, temp2);
	}
	
	memset(temp1, 0, ARRAY_SIZE);
	memset(temp2, 0, ARRAY_SIZE);
	
	strncpy(temp1, solution, leftPar);
    strncpy(temp2, solution + rightPar + 1, strlen(solution) - 1);
	
	strcpy(solution, temp1);
	strcat(solution, inner);
  	strcat(solution, temp2);
}
 
