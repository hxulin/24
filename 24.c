typedef struct data
{
    double num;
    struct data *next;
} Data;

typedef struct symbol
{
    char oper;
    struct symbol *next;
} Symbol;

void pushData(Data **s, double e)
{
    Data *p;
    p = (Data *) malloc (sizeof(Data));
    p->num = e;
    p->next = *s;
    *s = p;
}

void popData(Data **s, double *e)
{
    Data *p = *s;
    *e = (*s)->num;
    (*s) = (*s)->next;
    free(p);
}

void pushSymbol(Symbol **s, char c)
{
    Symbol *p;
    p = (Symbol *) malloc (sizeof(Symbol));
    p->oper = c;
    p->next = *s;
    *s = p;
}

void popSymbol(Symbol **s, char *c)
{
    Symbol *p = *s;
    *c = (*s)->oper;
    *s = (*s)->next;
    free(p);
}

void compute(Data **s, double d, char c, double e)
{
    double result;
    Data *p;
    switch(c)
    {
        case '+' :
	        result = d + e;
	        break;
        case '-' :
            result = d - e;
            break;
        case '*' :
            result = d * e;
            break;
        case '/' :
            result = d / e;
            break;
    }
    p = (Data *) malloc (sizeof(Data));
    p->num = result;
    p->next = *s;
    *s = p;
}

double eval(char *expression)
{
	int i = 0, tag = 1, index = 0;
    double d, e;
    char c, x, str[ARRAY_SIZE];
    Data *dh = NULL;
    Symbol *sh = NULL;
    while(1)
	{
		if(tag)
		{
			c = expression[index++];
		}
		else
		{
			tag = 1;
		} 
		if((c >= '0' && c <= '9') || '.' == c)
		{
			do
			{
				str[i++] = c;
				str[i] = '\0';
				c = expression[index++];
			} while((c >= '0' && c <= '9') || '.' == c);
			tag = 0;
			i = 0;
			d = atof(str);
			pushData(&dh, d);
		}
		else if('\0' == c)
		{
			while(sh)
			{
				popData(&dh, &e);
				popData(&dh, &d);
				popSymbol(&sh, &c);
				compute(&dh, d, c, e);
			}
			break;
		}
		else if('(' == c)
		{
			pushSymbol(&sh, c);
			if('-' == (c = expression[index++]))
			{
				pushData(&dh, 0);
				pushSymbol(&sh, c);
			}
			else
			{
				tag = 0;
			}
		}
		else if(')' == c)
		{
			while(1)
			{
				popSymbol( &sh, &x);
				if('(' == x)
				{ 
					break;
				} 
				popData(&dh, &e);
				popData(&dh, &d);
				compute(&dh, d, x, e);
			}
		}
		else if('*' == c || '/' == c)
		{
			if(!sh || '+' == sh->oper || '-' == sh->oper)
			{
				pushSymbol( &sh, c);
			}
			else if('*' == sh->oper || '/' == sh->oper)
			{
				popData(&dh, &e);
				popData(&dh, &d);
				popSymbol(&sh, &x);
				compute(&dh, d, x, e);
				tag = 0;
			}
			else if('(' == sh->oper)
			{
				pushSymbol(&sh, c);
			}
		}
		else if('+' == c || '-' == c)
		{
			if(!sh)
			{
				if(!dh)
				{
					pushData(&dh, 0);
				}
				pushSymbol( &sh, c);
			}
			else if('+' == sh->oper || '-' == sh->oper || '*' == sh->oper || '/' == sh->oper)
			{
				popData(&dh, &e);
				popData(&dh, &d);
				popSymbol(&sh, &x);
				compute(&dh, d, x, e);
				tag = 0;
			}
			else if('('==sh->oper)
			{
				pushSymbol( &sh, c);
			}
		}
	}
	popData(&dh, &e);
    return e;
}

